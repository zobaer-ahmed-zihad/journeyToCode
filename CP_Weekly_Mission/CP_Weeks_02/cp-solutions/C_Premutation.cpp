#include <bits/stdc++.h>
using namespace std;
void solve(int n)
{
    int ar[n - 1];
    for (int i = 0; i < n - 1; i++)
    {
        cin >> ar[i];
    }
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        for (int i = 0; i < n; i++)
        {
            solve(n);
        }
    }

    return 0;
}